# 1. Считываем строку с клавиатуры
input_string = input("Введите произвольную строку: ")

# 2. Заменяем все символы двоеточия на символ процента
# Используем метод replace() для замены и подсчета количества замен
modified_string = input_string.replace(":", "%")

# 3. Подсчитываем количество замен
# Количество замен = разница между длиной исходной строки и строки после замены
# (так как replace() заменяет все вхождения)
count_replacements = input_string.count(":")

# 4. Выводим результат
print(f"Измененная строка: {modified_string}")
print(f"Количество замен: {count_replacements}")